int32_t q30_mul(int32_t a, int32_t b)
{
    return (int32_t)(((int64_t)a * b) >> 30);
}

int32_t q30_asin(int32_t x)
{
    int32_t abs_x;
    int32_t index;
    int32_t result;

    if (x > Q30) {
        x = Q30;
    } else if (x < -Q30) {
        x = -Q30;
    }

    abs_x = (x < 0) ? -x : x;
    index = (int32_t)(((int64_t)abs_x * 180) / Q30);
    if (index > 180) {
        index = 180;
    }

    result = asin_lut[index];
    return (x < 0) ? -result : result;
}

int32_t q30_atan2(int32_t y, int32_t x)
{
    int32_t ax;
    int32_t ay;
    int32_t angle;
    int32_t ratio_idx;

    if (x == 0 && y == 0) {
        return 0;
    }

    ax = (x < 0) ? -x : x;
    ay = (y < 0) ? -y : y;

    if (ax >= ay) {
        ratio_idx = (int32_t)(((int64_t)ay * (ATAN_LUT_SIZE - 1)) / ax);
        angle = atan_lut[ratio_idx];
    } else {
        ratio_idx = (int32_t)(((int64_t)ax * (ATAN_LUT_SIZE - 1)) / ay);
        angle = Q30_HALF_PI - atan_lut[ratio_idx];
    }

    if (x >= 0) {
        if (y < 0) {
            angle = -angle;
        }
    } else {
        if (y >= 0) {
            angle = Q30_PI - angle;
        } else {
            angle = -Q30_PI + angle;
        }
    }

    return angle;
}
